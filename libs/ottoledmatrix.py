    def __init__(self, scl, sda):
        self.i2c = I2C(scl=Pin(scl), sda=Pin(sda))
        self.display = HT16K33MatrixFeatherWing(self.i2c)
        self.display.set_brightness(2)

    def MatrixDot(self, x, y, value):
        if(y >= 8):
            y -= 8
            x += 8
        if(value == '1'):
            self.display.plot(x, y)

        self.display.draw()

    def OttoDotMatrix(self, value):
        for i in range(128):
            x = math.floor(i / 16)
            y = i % 16
            if(y >= 8):
                y -= 8
                x += 8
            if(value[i] == '1'):
                self.display.plot(x, y)
        self.display.draw()
    
    def OttoEyes(self, imageIndex):
        eye = self.eyes_list[imageIndex]
        self.OttoDotMatrix(eye)
    
    def OttoMouth(self, imageIndex):
        mouth = self.mouth_list[imageIndex]
        self.OttoDotMatrix(mouth)

    def MatrixClear(self):
        self.display.clear().draw()
