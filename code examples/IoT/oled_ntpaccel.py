def wheel(pos):
   if pos < 0 or pos > 255:
      return (0, 0, 0)
   if pos < 85:
      return (255 - pos * 3, pos * 3, 0)
   if pos < 170:
       pos -= 85
       return (0, 255 - pos * 3, pos * 3)
   pos -= 170
   return (pos * 3, 0, 255 - pos * 3)

