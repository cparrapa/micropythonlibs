def sq(xx,yy,sz,c):
    # Draw a square at (xx,yy) with side sz and colour c
    # Used in character routine    
    oled.rect(xx,yy,sz,sz,c,1)
    
def character(asc,xt,yt,sz,c):
    # Single character at (xt,yt) of size sz: 1 to 4
    # and colour c
    code = asc * 5    # 5 bytes per character
    for ii in range(5): 
        line = FONT[code + ii]
        for yy in range(8):            
            if (line >> yy) & 0x1:
                sq(ii*sz+xt,yy*sz+yt,sz,c)
                    
def prnt_st(asci,xx,yy,sz,c):
    # Text string at (xx, yy) with size sz and colour c
    if sz == 1: move = 6
    if sz == 2: move = 11
    if sz == 3: move = 17
    if sz == 4: move = 22
    for letter in(asci):
        asci = ord(letter)
        character(asci,xx,yy,sz,c)
        xx = xx + move

def cntr_st(s,y,sz,c): 
    # centred text on line y, sizs sz and colour c
    if sz == 1: w = 6
    if sz == 2: w = 11
    if sz == 3: w = 17
    if sz == 4: w = 22
    gap = int((WIDTH - len(s) * w)/2)
    prnt_st(s,gap,y,sz,c)

